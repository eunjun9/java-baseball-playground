public class Calculator {

    public int plus(int a, int b) {
        return a+b; // Alt + Insert -> Test로 테스트코드 자동 완성 가능
    }

    public int minus(int a, int b) {
        return a-b;
    }

    public int multiply(int a, int b) {
        return a*b;
    }

    public int division(int a, int b) {
        return a/b;
    }
}
